    {

        $request->validate([
            'vardas' => 'required',
            'telefonas' => 'required',
            'pastas' => 'required',
            'klausimas' => 'required',
        ]);
            
        Klausimas::create([
            'vardas' => $request['vardas'],
            'telefonas' => $request['telefonas'],
            'pastas' => $request['pastas'],
            'klausimas' => $request['klausimas']
        ]);
         return redirect()->route('kont');
    }

